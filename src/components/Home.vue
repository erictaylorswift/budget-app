<template>
    <div class="flex-column" id="home">
        <h2 class="header">Budget for {{ budgets[0].start | formatDate }} to {{ budgets[0].end | formatDate }}</h2>
        <div class="flex">
            <div class="nes-container">
                <h2 class="title">Breakdown</h2>
                <div>
                    <p>Budgeted income: {{ budgets[0].income | formatCurrency }}</p>
                    <p>Budgeted expenses: {{ budgets[0].bills | formatCurrency }}</p>      
                    <hr>
                    <p>Budget net income: {{ budgets[0].income - budgets[0].bills | formatCurrency }}</p>
                    <p>Current net income: {{ income - expTotal | formatCurrency}}</p>
                </div>
            </div>
            <div class="nes-container">
                <h3 class="title">Perfomance</h3>
                <div class="messages">
                    <div class="message -left flex align-baseline" v-if="((income - expTotal) - (budgets[0].income - budgets[0].bills)) > 0">
                        <i class="nes-icon trophy is-medium"></i>
                        <div class="nes-balloon from-left">
                            <p style="color: green">
                                {{ (income - expTotal) - (budgets[0].income - budgets[0].bills) | formatCurrency }} ahead of budget
                            </p>
                        </div>
                    </div>
                    <div class="message -left flex align-baseline" v-else>
                        <i class="nes-bulbasaur is-medium"></i>
                        <div class="nes-balloon from-left">
                            <p>
                                {{ (income - expTotal) - (budgets[0].income - budgets[0].bills) | formatCurrency }} behind budget
                            </p>
                        </div>
                    </div>
                </div>
            </div>
        </div>
        <div class="flex">
            <div class="nes-container flex">
                <i class="nes-mario"></i>
                <div class="lists">
                    <h3>Expenses</h3>
                    <ul class="nes-list is-disc">
                        <li>
                            Spent: {{ expTotal | formatCurrency }}
                        </li>
                        <li>
                            Remaining: <b>{{ Number(budgets[0].bills) - expTotal | formatCurrency }}</b>
                        </li>
                    </ul>
                </div>
                <div class="lists">
                    <h3>Income</h3>
                    <ul class="nes-list is-disc">
                        <li>
                            Earned: {{ income | formatCurrency }}
                        </li>
                        <li>
                            Balance: <b>{{ income - budgets[0].income | formatCurrency }}</b>
                        </li>
                    </ul>
                </div>
            </div>
        </div>
    </div>
</template>

<script>
import { mapState } from 'vuex';
import moment from 'moment';
const numeral = require('numeral');

export default {
    computed: {
        ...mapState(['budgets', 'expTotal', 'income'])
    },
    filters: {
        formatDate(val) {
            let date = moment(val).format('MMM Do, YYYY')
            return date
        },
        formatCurrency(val) {
            let value = numeral(val).format('$0,0')
            return value
        }
    },
    data() {
        return {
            
        }
    },
    methods: {
       
    }
}
</script>

